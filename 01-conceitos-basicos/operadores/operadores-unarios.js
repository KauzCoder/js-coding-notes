//  Operadores Unários
// Operadores sao queles que atuam sobre um único operando. Eles realizam operações simples, como conversão de tipo, negação numérica, incremento e decremento.

console.log("\n=== Unários ===");
let n = 5;
console.log(+n);  // 5 (conversão de tipo)
console.log(-n);  // -5 (negação numérica)
n++;
console.log(n);   // 6 (incremento)
n--;
console.log(n);   // 5 (decremento)