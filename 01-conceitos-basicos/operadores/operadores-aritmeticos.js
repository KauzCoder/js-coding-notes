/*
Operadores Aritméticos

Os operadores aritméticos são usados para realizar operações matemáticas básicas entre números.
Eles são fundamentais para cálculos, manipulação de valores e lógica de programação.

1️⃣ Operadores Principais
------------------------
+   → Adição          (a + b)
-   → Subtração       (a - b)
*   → Multiplicação   (a * b)
/   → Divisão         (a / b)
%   → Resto da divisão (a % b)
**  → Exponenciação   (a ** b)
*/

// Exemplo de uso dos operadores aritméticos
let a = 10;
let b = 3;
console.log("=== Aritméticos ===");
console.log(a + b); // Soma → 13
console.log(a - b); // Subtração → 7
console.log(a * b); // Multiplicação → 30
console.log(a / b); // Divisão → 3.333...
console.log(a % b); // Módulo → 1 (resto)
console.log(a ** b); // Exponenciação → 1000